import math 
angle = math.pi / 4  # 45 degrees in radians

print(f"Sine of 45 degrees: {math.sin(angle)}")  # Output: 0.7071067811865475
print(f"Cosine of 45 degrees: {math.cos(angle)}")  # Output: 0.7071067811865476
print(f"Tangent of 45 degrees: {math.tan(angle)}")  # Output: 0.9999999999999999
print(f"Arc sine of 0.7071: {math.asin(0.7071)}")  # Output: 0.7853981633974483 (approx 45 degrees in radians)
