import math

x = 1

print(f"Hyperbolic sine of {x}: {math.sinh(x)}")  # Output: 1.1752011936438014
print(f"Hyperbolic cosine of {x}: {math.cosh(x)}")  # Output: 1.5430806348152437
print(f"Hyperbolic tangent of {x}: {math.tanh(x)}")  # Output: 0.7615941559557649
print(f"Inverse hyperbolic sine of {x}: {math.asinh(x)}")  # Output: 0.881373587019543
print(f"Inverse hyperbolic cosine of {x}: {math.acosh(1.5430806348152437)}")  # Output: 1.0
print(f"Inverse hyperbolic tangent of {x-0.1}: {math.atanh(0.9)}")  # Output: 1.4722194895832204

