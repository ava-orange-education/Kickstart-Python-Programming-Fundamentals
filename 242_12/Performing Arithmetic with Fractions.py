from fractions import Fraction

frac1 = Fraction(3, 4)
frac2 = Fraction(1, 2)

# Arithmetic operations with fractions
addition = frac1 + frac2
subtraction = frac1 - frac2
multiplication = frac1 * frac2
division = frac1 / frac2

print(f"Addition: {addition}")  # Output: 5/4
print(f"Subtraction: {subtraction}")  # Output: 1/4
print(f"Multiplication: {multiplication}")  # Output: 3/8
print(f"Division: {division}")  # Output: 3/2
