# Import modules from the numeric_ops package 
from numeric_ops.arithmetic import add, divide 
from numeric_ops.exponentials import power, square_root 
from numeric_ops.logarithms import natural_log, log_base_ten 
# Using the functions from the numeric_ops package 
print(f"Addition: {add(10, 5)}") 
print(f"Division: {divide(10, 0)}") 
print(f"Power: {power(2, 3)}") 
print(f"Square Root: {square_root(16)}") 
print(f"Natural Log: {natural_log(-1)}") 
print(f"Log Base Ten: {log_base_ten(100)}")
