#Creating and Initializing Strings

# Single quotes
single_quote_string = 'Hello, World!'

# Double quotes
double_quote_string = "Hello, World!"

# Triple quotes for multi-line strings
multi_line_string = '''This is a
multi-line string.'''

#empty String
empty_string = ""

#Accessing Characters and Slicing Strings
my_string = "Python"

# Accessing characters by positive index
first_char = my_string[0]  # 'P'
second_char = my_string[1]  # 'y'

# Accessing characters by negative index
last_char = my_string[-1]  # 'n'
second_last_char = my_string[-2]  # 'o'

my_string = "Hello, World!"

# Slicing from index 0 to 4 (not including 5)
substring = my_string[0:5]  # 'Hello'

# Slicing with a step
every_second_char = my_string[0:11:2]  # 'Hlo o'

#String Concatenation and Repetition

string1 = "Hello"
string2 = "World"

# Concatenating strings
greeting = string1 + ", " + string2 + "!"  # 'Hello, World!'

# Repeating strings
repeat_hello = "Hello" * 3  # 'HelloHelloHello'




