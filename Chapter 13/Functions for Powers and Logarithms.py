import math

x = 10
y = 2

print(f"{x} raised to the power of {y}: {math.pow(x, y)}")  # Output: 100.0
print(f"Square root of {x}: {math.sqrt(x)}")  # Output: 3.1622776601683795
print(f"Exponential of {y}: {math.exp(y)}")  # Output: 7.3890560989306495
print(f"Natural logarithm of {x}: {math.log(x)}")  # Output: 2.302585092994046
print(f"Base-10 logarithm of {x}: {math.log10(x)}")  # Output: 1.0
