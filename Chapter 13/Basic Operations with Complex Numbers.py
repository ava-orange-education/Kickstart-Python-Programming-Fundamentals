# Basic operations with complex numbers
z1 = 2 + 3j
z2 = 4 - 2j

addition = z1 + z2
subtraction = z1 - z2
multiplication = z1 * z2
division = z1 / z2

print(f"Addition: {addition}")  # Output: (6+1j)
print(f"Subtraction: {subtraction}")  # Output: (-2+5j)
print(f"Multiplication: {multiplication}")  # Output: (14+10j)
print(f"Division: {division}")  # Output: (0.2+0.7j)
